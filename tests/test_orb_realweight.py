def test_encoder(gw, device):
    from tt_atom.orb_model import Encoder, _to_dev
    import ttnn

    cfg = gw.config
    w = gw.weights
    enc = Encoder(w, device, node_in=cfg["node_embed_size"], edge_in=cfg["edge_embed_size"],
                 latent_dim=cfg["latent_dim"], hidden_dim=1024)

    node_feat = gw.host("node_feat")
    edge_feat = gw.host("edge_feat")
    node_dev = _to_dev(node_feat, device, ttnn.bfloat16)
    edge_dev = _to_dev(edge_feat, device, ttnn.bfloat16)

    node_out, edge_out = enc(node_dev, edge_dev)
    node_out = ttnn.to_torch(node_out).float()
    edge_out = ttnn.to_torch(edge_out).float()

    gold_node = gw.activation("encoder.out0")
    gold_edge = gw.activation("encoder.out1")

    pcc_node = _pcc(node_out, gold_node)
    pcc_edge = _pcc(edge_out, gold_edge)
    print(f"\n[orb] encoder node PCC={pcc_node:.6f} edge PCC={pcc_edge:.6f}")
    assert pcc_node > 0.999, pcc_node
    assert pcc_edge > 0.999, pcc_edge

