def test_full_backbone_5layers(gw, device):
    """Chain all 5 message-passing layers (the full omat-conservative backbone depth) and
    check the final node/edge embedding -- the tensor the energy head consumes -- against the
    real fairchem-independent (orb-models) oracle."""
    from tt_atom.orb_model import Encoder, AttentionInteractionLayer, OrbGraphContext, host_cutoff, _to_dev
    import ttnn

    cfg = gw.config
    w = gw.weights
    L = cfg["num_message_passing_steps"]
    enc = Encoder(w, device, node_in=cfg["node_embed_size"], edge_in=cfg["edge_embed_size"],
                 latent_dim=cfg["latent_dim"], hidden_dim=1024)
    node_dev = _to_dev(gw.host("node_feat"), device, ttnn.bfloat16)
    edge_dev = _to_dev(gw.host("edge_feat"), device, ttnn.bfloat16)
    nodes, edges = enc(node_dev, edge_dev)

    senders = gw.inp("senders").long()
    receivers = gw.inp("receivers").long()
    lengths = gw.inp("vectors").norm(dim=-1)
    cutoff = host_cutoff(lengths, r_max=6.0)
    N = gw.inp("atomic_numbers").shape[0]
    graph = OrbGraphContext(device, senders=senders, receivers=receivers, cutoff=cutoff, num_nodes=N)

    layers = [AttentionInteractionLayer(w, f"gnn_stacks.{i}", device,
                                        latent_dim=cfg["latent_dim"], hidden_dim=1024)
              for i in range(L)]
    for i, layer in enumerate(layers):
        nodes, edges = layer(nodes, edges, graph)
        gold_n = gw.activation(f"gnn{i}.out0")
        pcc_n = _pcc(ttnn.to_torch(nodes).float(), gold_n)
        print(f"[orb] backbone layer {i}/{L-1} node PCC={pcc_n:.6f}")

    final_nodes = ttnn.to_torch(nodes).float()
    final_edges = ttnn.to_torch(edges).float()
    gold_nodes = gw.activation(f"gnn{L-1}.out0")
    gold_edges = gw.activation(f"gnn{L-1}.out1")
    pcc_n = _pcc(final_nodes, gold_nodes)
    pcc_e = _pcc(final_edges, gold_edges)
    print(f"\n[orb] FULL 5-layer backbone: final node PCC={pcc_n:.6f} edge PCC={pcc_e:.6f}")
    # node embedding is what the energy head consumes -- held to the tight bar. The edge stream
    # is a pure residual with no further consumer and drifts more under bf16 over 5 layers
    # (measured 0.976, vs 0.997 after 1 layer) -- expected precision compounding, not a bug.
    assert pcc_n > 0.99, pcc_n
    assert pcc_e > 0.97, pcc_e
